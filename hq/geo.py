def resolve_centroid(iso2: str) -> Optional[Tuple[float, float]]:
    """Return (lat, lng) for a country ISO2 code, or None."""
    if not iso2:
        return None
    entry = COUNTRY_CENTROIDS.get(iso2.upper())
    if not entry:
        return None
    return (entry[0], entry[1])


def country_name(iso2: str) -> str:
    """Return the english country name for an ISO2 code, or the code itself."""
    if not iso2:
        return ""
    entry = COUNTRY_CENTROIDS.get(iso2.upper())
    return entry[2] if entry else iso2.upper()
